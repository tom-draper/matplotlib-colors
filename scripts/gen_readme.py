

def colors():
    return """# Colors\n\nColors"""


def source():
    return """## Source\n\nA small portion of these color themes were sourced from color design websites, including <a href="https://coolors.co/">coolors.co</a> and <a href="https://www.canva.com/">canva.com</a>. Thanks!"""


def notice():
    return """## Notice\n\nThis file was autogenerated by `gen_readme.py`."""


def build_readme():
    readme = '\n\n'.join([colors(),  source(), notice()])
    return readme


if __name__ == '__main__':
    readme = build_readme()
    with open('matplotlib_colors/README.md', 'w') as f:
        f.write(readme)
